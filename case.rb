weather = 'foggy'

case weather
when 'sunny'
  puts "Don't forget your sunglasses."
when 'rainy'
  puts "Don't forget your umbrella and Hunter boots."
when 'foggy'
  puts "Stay home, make some tea, and read a good book."
when 'cloudy'
  puts "Stay home and have a Netflix marathon."
when 'snowing'
  puts "Don't forget your coat, hat, scarf, gloves... Actually, unless you're building a snowman, stay inside and drink some hot chocolate."
end
  